int map_get_cell(int x, int y)
{
    return map.cells[y][x];
}

void map_set_cell(int x, int y, int tile_type)
{
    map.cells[y][x] = tile_type;
}

int map_get_bonus(int x, int y)
{
    return map.bonuses[y][x];
}

void map_print()
{
    for (int y = 0; y < MAP_HEIGHT; y++)
    {
        for (int x = 0; x < MAP_WIDTH; x++)
        {
            printf("%d ", map.cells[y][x]);
        }

        printf("\n");
    }
}

void map_print_bonuses()
{
    for (int y = 0; y < MAP_HEIGHT; y++)
    {
        for (int x = 0; x < MAP_WIDTH; x++)
        {
            printf("%d ", map.bonuses[y][x]);
        }

        printf("\n");
    }
}